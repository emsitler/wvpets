class Pet {
    constructor(name, type) {
        this.name = name;
        switch(type) {
            case 1:
                this.image = "..."
                this.animal = "Raccoon"
                break;
            case 2:
                this.image = "..."
                this.animal = "Bear"
                break;
            case 3:
                this.image = "..."
                this.animal = "Blue Jay"
                break;
            case 4:
                this.image = "..."
                this.animal = "Bobcat"
                break;
            default:
                this.image = "..."
                this.animal = "Raccoon"
                break;
        }
    }


}